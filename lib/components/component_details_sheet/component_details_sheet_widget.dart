                    if (snapshot.data!.isEmpty) {
                      return Container();
                    }
                    final columnComponentContentRecord =
                        columnComponentContentRecordList.isNotEmpty
                            ? columnComponentContentRecordList.first
                            : null;
                    return Column(
                      mainAxisSize: MainAxisSize.max,
                      crossAxisAlignment: CrossAxisAlignment.stretch,
                      children: [
                        Padding(
                          padding: const EdgeInsetsDirectional.fromSTEB(
                              0.0, 20.0, 0.0, 20.0),
                          child: Builder(
                            builder: (context) {
                              final listValue = columnComponentContentRecord
                                      ?.listOptions
                                      .toList() ??
                                  [];
                              return Wrap(
                                spacing: 10.0,
                                runSpacing: 10.0,
                                alignment: WrapAlignment.start,
                                crossAxisAlignment: WrapCrossAlignment.start,
                                direction: Axis.horizontal,
                                runAlignment: WrapAlignment.start,
                                verticalDirection: VerticalDirection.down,
                                clipBehavior: Clip.none,
                                children: List.generate(listValue.length,
                                    (listValueIndex) {
                                  final listValueItem =
                                      listValue[listValueIndex];
                                  return Row(
                                    mainAxisSize: MainAxisSize.min,
                                    children: [
                                      InkWell(
                                        splashColor: Colors.transparent,
                                        focusColor: Colors.transparent,
                                        hoverColor: Colors.transparent,
                                        highlightColor: Colors.transparent,
                                        onTap: () async {
                                          if (columnComponentContentRecord
                                              .listContent
                                              .contains(listValueItem)) {
                                            await columnComponentContentRecord
                                                .reference
                                                .update({
                                              ...mapToFirestore(
                                                {
                                                  'list_content':
                                                      FieldValue.arrayRemove(
                                                          [listValueItem]),
                                                },
                                              ),
                                            });
                                          } else {
                                            await columnComponentContentRecord
                                                .reference
                                                .update({
                                              ...mapToFirestore(
                                                {
                                                  'list_content':
                                                      FieldValue.arrayUnion(
                                                          [listValueItem]),
                                                },
                                              ),
                                            });
                                          }
                                        },
                                        child: Material(
                                          color: Colors.transparent,
                                          elevation: 2.0,
                                          shape: RoundedRectangleBorder(
                                            borderRadius:
                                                BorderRadius.circular(15.0),
                                          ),
                                          child: Container(
                                            height: 40.0,
                                            decoration: BoxDecoration(
                                              color:
                                                  columnComponentContentRecord!
                                                          .listContent
                                                          .contains(
                                                              listValueItem)
                                                      ? FlutterFlowTheme.of(
                                                              context)
                                                          .secondary
                                                      : FlutterFlowTheme.of(
                                                              context)
                                                          .secondaryBackground,
                                              borderRadius:
                                                  BorderRadius.circular(15.0),
                                            ),
                                            child: Align(
                                              alignment: const AlignmentDirectional(
                                                  0.0, 0.0),
                                              child: Padding(
                                                padding: const EdgeInsetsDirectional
                                                    .fromSTEB(
                                                        10.0, 0.0, 10.0, 0.0),
                                                child: Text(
                                                  listValueItem,
                                                  style: FlutterFlowTheme.of(
                                                          context)
                                                      .bodyMedium,
                                                ),
                                              ),
                                            ),
                                          ),
                                        ),
                                      ),
                                    ],
                                  );
                                }),
                              );
                            },
                          ),
                        ),
                        FFButtonWidget(
                          onPressed: () async {
                            Navigator.pop(context);
                          },
                          text: 'Continue',
                          options: FFButtonOptions(
                            height: 40.0,
                            padding: const EdgeInsetsDirectional.fromSTEB(
                                24.0, 0.0, 24.0, 0.0),
                            iconPadding: const EdgeInsetsDirectional.fromSTEB(
                                0.0, 0.0, 0.0, 0.0),
                            color: FlutterFlowTheme.of(context).primary,
                            textStyle: FlutterFlowTheme.of(context)
                                .titleSmall
                                .override(
                                  fontFamily: 'Manrope',
                                  color: Colors.white,
                                ),
                            elevation: 3.0,
                            borderSide: const BorderSide(
                              color: Colors.transparent,
                              width: 1.0,
                            ),
                            borderRadius: BorderRadius.circular(8.0),
                          ),
                        ),
                      ],
                    );
                  },
                ),
              );
            } else {
              return Container(
                width: 100.0,
                height: 100.0,
                decoration: BoxDecoration(
                  color: FlutterFlowTheme.of(context).secondaryBackground,
                ),
              );
            }
          },
        ),
      ),
    );
  }
